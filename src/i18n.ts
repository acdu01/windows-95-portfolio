type TranslationKey = keyof typeof translations.en

const resolveEntry = (entry: TranslationEntry, params?: Record<string, string | number>) => {
  if (typeof entry === 'function') {
    return entry(params ?? {})
  }
  return entry
}

export function useI18n() {
  const language = useSettingsStore((state) => state.language)

  const t = (key: TranslationKey, params?: Record<string, string | number>) => {
    const localized = translations[language][key] ?? translations.en[key]
    return resolveEntry(localized, params)
  }

  return { language, t }
}
